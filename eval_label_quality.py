import os
import glob
import os.path as osp
import torch
import numpy as np
from opencood.utils import eval_utils, box_utils, dataset_utils
from tqdm import tqdm
import argparse
import random


def parse_config():
    parser = argparse.ArgumentParser(description='Evaluate pseudo-label quality against ego GT')
    parser.add_argument('--root_dir', type=str, required=True,
                        help='Root directory of the V2V4Real dataset')
    parser.add_argument('--pseudo_label_path', type=str, required=True,
                        help='Relative path (from root_dir) to the pseudo-label directory')
    parser.add_argument('--pseudo_label_idx', type=str, default='pred',
                        help='Filename suffix for the pseudo-label .npy files (default: pred)')
    parser.add_argument('--gt_label_path', type=str, default=None,
                        help='Path to directory containing GT .npy files '
                             '(generated by: python test.py --ego_car_id 0 --save_npy). '
                             'Defaults to <root_dir>/initial_seed_include_refcar')
    parser.add_argument('--gt_label_idx', type=str, default='gt',
                        help='Filename suffix for GT .npy files (default: gt, matching test.py output)')
    parser.add_argument('--min_timestep', type=int, default=0)
    parser.add_argument('--max_frames', type=int, default=10000)

    args = parser.parse_args()

    random.seed(310)
    np.random.seed(310)
    torch.manual_seed(310)

    return args


def main():
    args = parse_config()

    gt_label_path = args.gt_label_path or os.path.join(args.root_dir, 'initial_seed_include_refcar')
    print('pseudo_label_path:', args.pseudo_label_path)
    print('gt_label_path:', gt_label_path)

    scenario_folders = dataset_utils.get_scenario_folders(
        root_dir=args.root_dir,
        train_split='subset2',
        data_split='train',
    )

    save_path = os.path.join(args.root_dir, "label_quality_eval")
    os.makedirs(save_path, exist_ok=True)
    print(f'Results will be saved to: {save_path}')

    eval_thresh = [0.3, 0.5, 0.7]
    result_stat = {}
    result_stat_short = {}
    result_stat_middle = {}
    result_stat_long = {}
    for cur_thresh in eval_thresh:
        result_stat[cur_thresh] = {"tp": [], "fp": [], "gt": 0}
        result_stat_short[cur_thresh] = {"tp": [], "fp": [], "gt": 0}
        result_stat_middle[cur_thresh] = {"tp": [], "fp": [], "gt": 0}
        result_stat_long[cur_thresh] = {"tp": [], "fp": [], "gt": 0}

    i = 0
    for scen_idx, scenario_path in tqdm(enumerate(scenario_folders)):
        if i == args.max_frames:
            break

        num_timestamps = len(glob.glob(osp.join(scenario_path, '0', '*.pcd')))
        traintest, scenario = scenario_path.split('/')[-2], scenario_path.split('/')[-1]

        for timestamp in range(num_timestamps):
            if i == args.max_frames:
                break
            if timestamp < args.min_timestep:
                continue

            i += 1
            gt_center_np = np.load(
                osp.join(
                    gt_label_path,
                    traintest, scenario, str(0), f"{timestamp:06d}_{args.gt_label_idx}.npy"
                )
            )
            gt_np = box_utils.boxes_to_corners_3d(gt_center_np, order='lwh')

            pred_center_np = np.load(
                osp.join(
                    args.root_dir, args.pseudo_label_path,
                    traintest, scenario, str(0), f"{timestamp:06d}_{args.pseudo_label_idx}.npy"
                )
            )

            if pred_center_np.ndim == 3:
                pred_center_np = pred_center_np.squeeze(1)

            if gt_center_np.shape[0] == 0:
                gt_t = None
            else:
                gt_t = torch.from_numpy(gt_np)

            if pred_center_np.shape[0] == 0:
                pred_t = None
                pred_score_all_one = None
            else:
                pred_np = box_utils.boxes_to_corners_3d(pred_center_np, order='lwh')
                pred_t = torch.from_numpy(pred_np)
                pred_score_all_one = torch.ones(pred_t.shape[0])

            num_gt_bbox = gt_center_np.shape[0]
            num_pred_bbox = pred_center_np.shape[0]
            print(f'{i:04d} | GT/Pred boxes: {num_gt_bbox:03d}/{num_pred_bbox:03d}')

            for cur_thresh in eval_thresh:
                eval_utils.calculate_tp_fp(
                    pred_t, pred_score_all_one, gt_t, result_stat, cur_thresh
                )
                eval_utils.calculate_tp_fp(
                    pred_t, pred_score_all_one, gt_t, result_stat_short, cur_thresh,
                    left_range=0, right_range=30,
                )
                eval_utils.calculate_tp_fp(
                    pred_t, pred_score_all_one, gt_t, result_stat_middle, cur_thresh,
                    left_range=30, right_range=50,
                )
                eval_utils.calculate_tp_fp(
                    pred_t, pred_score_all_one, gt_t, result_stat_long, cur_thresh,
                    left_range=50, right_range=80,
                )

    eval_utils.eval_final_results(result_stat, save_path, eval_thresh, "all")
    eval_utils.eval_final_results(result_stat_short, save_path, eval_thresh, "short")
    eval_utils.eval_final_results(result_stat_middle, save_path, eval_thresh, "middle")
    eval_utils.eval_final_results(result_stat_long, save_path, eval_thresh, "long")


if __name__ == "__main__":
    main()
